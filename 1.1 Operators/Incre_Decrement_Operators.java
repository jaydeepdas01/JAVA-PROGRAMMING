public class Incre_Decrement_Operators {
    public static void main(String[] args) {
        int a = 10;

        System.out.println("Increment oprators : ");
        System.out.println(a++); // 10 (11) // first print then increment .
        System.out.println(a); // 11 after print increasing value
        System.out.println(++a); // 12 firset increase then print

         System.out.println("Decrement oprators : ");
        System.out.println(a--); // 12 (11) // first print then decrement .
        System.out.println(a); // 11 after print decreasing value
        System.out.println(--a); //10  12 firset decrease then print
    }
}
