public class Assignment_Operators {
    public static void main(String[] args) {
        int a;
        a = 10; // simple assign
        System.out.println("a = 10 : " + a);

        a += 10; // compound
        System.out.println("a += 10(a = a + 10) : " + a);
        System.out.println();
        System.out.println("Other compound Assignment operator : ");
        System.out.println("Variable_Name -= 10 (Variable_name = Variable - 10) ");
        System.out.println("Variable_Name *= 10 (Variable_name = Variable * 10) ");
        System.out.println("Variable_Name /= 10 (Variable_name = Variable / 10) ");
        System.out.println("Variable_Name %= 10 (Variable_name = Variable % 10) ");

    }
}
